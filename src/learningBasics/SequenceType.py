# List example
x = ["Pune","Mumbai","Nagpur"]
print(x)
print(type(x))

# tuple example
x = ("Pune","Mumbai","Nagpur")
print(x)
print(type(x))

# Set Example
x= {"Pune", "Mumbai","Nagpur"}
print(x)
print(type(x))


#frozenSet example
x = frozenset({"Pune","Mumbai","Nagpur"})
print(x)
print(type(x))

# Boolean value
x= True
print(x)